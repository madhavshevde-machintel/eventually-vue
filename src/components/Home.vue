<script>
import ShowEvents from './ShowEvents.vue'
import ShowRecentReviews from './ShowRecentReviews.vue'
import ShowResearchPopularEvents from './ShowResearchPopularEvents.vue'
export default {
    components: {
        'show-events': ShowEvents,
        'show-reviews': ShowRecentReviews,
        'show-research-popular-event': ShowResearchPopularEvents
    },
    data() {
        return {
            currentYear: new Date().getFullYear(),
            reviewsLoaded: 0,
            eventsLoaded: 0,
            subCategoriesLoaded: 0,
            searchText: '',
            searchFormSubmitted: false,
            newsLetterEmailID: '',
            newsLetterMsg: {required:true,email:true},
            newsLetterFormSubmitted: false
        }
    },
    inject : ['apiUrl'],
    watch: {
        newsLetterEmailID(value) {
            this.validateEmail(value);
        }
    },
    methods: {
        async subscribeNewsLetter() {
            this.newsLetterFormSubmitted = true;
            if (!(this.newsLetterMsg['required'] || this.newsLetterMsg['email'])) {
                let url = this.apiUrl + '/EventuallyHome/SubscribeNewsLetter'
                const response = await fetch(url, {
                    method: 'POST',
                    headers: {
                        'Content-Type':'application/json;charset=utf-8'
                    },
                    body: JSON.stringify({ subscribeEmailID: this.newsLetterEmailID })
                });
                this.newsLetterEmailID = null;
                this.newsLetterFormSubmitted = false;
                window.open('https://eventually.com/thanks-subscribe');
            }
        },
        validateEmail(emailID) {
            this.newsLetterMsg['required'] = false;
            if (!emailID) {
                this.newsLetterMsg['required'] = true;
            }
            this.newsLetterMsg['email'] = false;
            if (!/^([\w-\.]+)@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.)|(([\w-]+\.)+))([a-zA-Z]{2,12}|[0-9]{1,3})(\]?)$/.test(emailID)) {
                this.newsLetterMsg['email'] = true;
            }
        },
        search() {
            this.searchFormSubmitted = true;
            if (!this.searchText)
                return;
            window.open("https://eventually.com/search-result?q=" + this.searchText);
        },
        /* Reset webflow and other scripts only after all data is loaded.
           because otherwise animations don't work properly
        */ 
        async processJavaScripts(eventType) {
            if (eventType == 'events-loaded')
                this.eventsLoaded++;
            else if (eventType == 'reviews-loaded')
                this.reviewsLoaded++;
            else if (eventType == 'subcategories-loaded')
                this.subCategoriesLoaded++
            if (this.eventsLoaded == 5 && this.reviewsLoaded == 2 && this.subCategoriesLoaded == 6) {
                this.resetWebFlow();
                await this.$loadScript('https://v6.eventually.com/scripts/counter.js');
                await this.$loadScript("https://cdnjs.cloudflare.com/ajax/libs/OwlCarousel2/2.3.4/owl.carousel.min.js");
                await this.$loadScript("https://cdn.jsdelivr.net/npm/@finsweet/attributes-scrolldisable@1/scrolldisable.js");
                this.setSlider();
            }
        },
        resetWebFlow() {
            window.Webflow && window.Webflow.destroy();
            window.Webflow && window.Webflow.require("ix2").init();
            document.dispatchEvent(new Event("readystatechange"));
        },
        async loadScript(scriptUrl, async, defer) {
            const script = document.createElement('script');
            script.src = scriptUrl;
            script.type = 'text/javascript';
            if (async != null) {
                script.async = async;
            }
            if (defer != null) {
                script.defer = defer;
            }

            document.body.appendChild(script);
            return new Promise((res, rej) => {
                script.onload = function () {
                    res();
                }
                script.onerror = function () {
                    rej();
                }
            });
        },
        setSlider() {
            var owl = $(".owl-carousel");
            owl.owlCarousel({
                items: 5,
                margin: 3,
                loop: true,
                //lazyLoad: true,
                mouseDrag: false,
                pullDrag: false,
                touchDrag: false,
                nav: true,
                responsive: {
                    0: {
                        items: 1
                    },
                    600: {
                        items: 2
                    },
                    768: {
                        items: 3
                    },
                    1200: {
                        items: 5
                    },
                }
            });
            $(".card-item-hovered").mouseover(function () {
                $(this).parent().parent().addClass("upper");

            });
            $(".card-item-hovered").mouseout(function () {
                $(this).parent().parent().removeClass("upper");

            });
        }
    },
    setup() {
    },
    mounted() {
        const WEBFLOW_PAGE_ID = '62e7c62cda698584f27df1a6';
        const WEBFLOW_SITE_ID = '62e7c62cda69851a7c7df1a7';
        var doc = document.getElementsByTagName("html")[0]
        doc.setAttribute('data-wf-page', WEBFLOW_PAGE_ID)
        doc.setAttribute('data-wf-site', WEBFLOW_SITE_ID)
        document.title = 'Trusted Business Event Reviews by Your Peers | eventually.com';
    }
}
